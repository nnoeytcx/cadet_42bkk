#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	s;

	s = 1;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	while (n / s >= 10 && n / s > 0)
		s *= 10;
	while (n >= 0)
	{
		ft_putchar_fd(n / s + '0', fd);
		n -= (n / s) * s;
		s /= 10;
	}
}
