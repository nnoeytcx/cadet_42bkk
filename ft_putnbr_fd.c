#include "libft.h"

void	ft_putnbr(int nbr, int fd)
{
	int	s;

	s = 1;
	if (nbr == -2147483648)
		write(fd, "-2147483648", 11);
	if (nbr < 0)
	{
		write(fd, "-", 1);
		nbr *= -1;
	}
	while (nbr / s >= 10 && nbr / s > 0)
		s *= 10;
	while (nbr >= 0)
	{
		write(fd, nbr / s + '0', 1);
		nbr -= (nbr / s) * s;
		s /= 10;
	}
}
